package javeriana.edu.rest;

public class Article {
    
    String author;
    String title;


    public Article() {}

    public void setAuthor(String author) {
        this.author = author;
    }
    public String getAuthor() {
        return author;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }
}
